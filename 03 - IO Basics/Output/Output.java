public class Output {
    public static void main(String[] args) {
        // Using System.out.print
        System.out.print("Hello, World!"); // Does not append a newline

        // Using System.out.println
        System.out.println("Hello, World!"); // Appends a newline

        // Using System.out.format
        System.out.format("Hello, World!"); // Similar to print but allows formatting

        // Using System.out.printf
        System.out.printf("Hello, World!"); // Similar to format but allows formatting
    }
}

/*
- System.out.print prints the string without appending a newline character.
- System.out.println prints the string and appends a newline character at the end.
- System.out.format and System.out.printf are used for formatted output, allowing you to specify
  placeholders in the string and provide values to fill those placeholders.
- In this case, all four methods will output "Hello, World!" to the console.
*/
