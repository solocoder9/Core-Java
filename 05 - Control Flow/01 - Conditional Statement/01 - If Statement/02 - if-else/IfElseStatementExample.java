public class IfElseStatementExample {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        // If-else statement
        if (x > y) {
            System.out.println("x is greater than y");
        } else {
            System.out.println("x is not greater than y");
        }
    }
}

/*
- An if-else statement allows you to execute one block of code if a condition is true and another block if it's false.
- The condition inside the if statement is enclosed in parentheses ().
- If the condition is true, the code inside the if block is executed. If it's false, the code inside the else block is executed.
- In this example, the if-else statement checks if the value of x is greater than the value of y.
- If the condition is true, it prints "x is greater than y" to the console. Otherwise, it prints "x is not greater than y".
*/
