public class SimpleIfStatementExample {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        // Simple if statement
        if (x < y) {
            System.out.println("x is less than y");
        }
    }
}

/*
- An if statement is used to execute a block of code only if a specified condition is true.
- The condition inside the if statement is enclosed in parentheses ().
- If the condition is true, the code inside the if block is executed. If it's false, the code is skipped.
- In this example, the if statement checks if the value of x is less than the value of y.
- If the condition is true, it prints "x is less than y" to the console. Otherwise, nothing happens.
*/
