public class ForLoopExample {
    public static void main(String[] args) {
        // Using a for loop to print numbers from 1 to 5
        for (int i = 1; i <= 5; i++) {
            System.out.println("Count is: " + i);
        }
    }
}

/*
- A for loop is used to repeat a block of code a specific number of times.
- It consists of three parts: initialization, condition, and iteration.
- In this example, the loop initializes the variable i to 1, checks if i is less than or equal to 5, and increments i by 1 after each iteration.
- The loop continues to execute as long as the condition i <= 5 is true.
- Inside the loop, the value of i is printed, resulting in numbers from 1 to 5 being printed.
*/
