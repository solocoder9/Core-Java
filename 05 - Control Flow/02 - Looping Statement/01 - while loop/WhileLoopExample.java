public class WhileLoopExample {
    public static void main(String[] args) {
        int i = 1;
        // Using a while loop to print numbers from 1 to 5
        while (i <= 5) {
            System.out.println("Count is: " + i);
            i++;
        }
    }
}

/*
- A while loop is used to repeatedly execute a block of code as long as a specified condition is true.
- In this example, the loop iterates as long as the value of i is less than or equal to 5.
- Inside the loop, the value of i is printed, and then it is incremented by 1.
- The loop continues to execute until the condition i <= 5 is false, at which point the program exits the loop.
*/
