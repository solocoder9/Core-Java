public class LocalVariable {
    public static void main(String[] args) {

        // Declare a local variable
        int x = 5; 

        // Use the local variable
        System.out.println("The value of x is: " + x); // Output: The value of x is: 5
    }
}

/*
A local variable in Java is a variable that is declared inside a method or a block of code 
(such as a loop or a conditional statement). Local variables are only accessible within 
the method or block in which they are declared. 
In this example, the variable x is a local variable that is declared inside the main method.
*/
