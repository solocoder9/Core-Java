public class ArrayDeclaration {
    public static void main(String[] args) {

        // Array declareation without initialize direct values
        int[] arr = new int[5];

        // Assigning values to array elements (Initilization)
        arr[0] = 10;
        arr[1] = 20;
        arr[2] = 30;
        arr[3] = 40;
        arr[4] = 50;

        // Print the element of index 3
        System.out.print(arr[3]);
    }
}
