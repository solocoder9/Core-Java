public class Example {
    public static void main(String[] args) {
        // Declare and initialize a long variable
        long l = 10000000000L;

        // Print the value of the long variable
        System.out.println("The value of l is: " + l); // Output: The value of l is: 10000000000
    }
}

/*
- A long data type in Java is a primitive data type used to store integer values.
- It occupies 64 bits (8 bytes) of memory.
- It has a range from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807.
- Long variables are used when the range of int is not sufficient to store the required value.
- They are suitable for representing very large integers or for calculations that require a large range.
- The default value of a long variable is 0L.
*/
