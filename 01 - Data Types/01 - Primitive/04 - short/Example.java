public class Example {
    public static void main(String[] args) {
        // Declare and initialize a short variable
        short s = 1000;

        // Print the value of the short variable
        System.out.println("The value of s is: " + s); // Output: The value of s is: 1000
    }
}

/*
- A short data type in Java is a primitive data type used to store integer values.
- It occupies 16 bits (2 bytes) of memory.
- It has a range from -32,768 to 32,767.
- Short variables are useful when memory conservation is important, but the range of a byte is not sufficient.
- They are commonly used in situations where memory usage needs to be minimized, such as when working with large arrays.
- The default value of a short variable is 0.
*/
