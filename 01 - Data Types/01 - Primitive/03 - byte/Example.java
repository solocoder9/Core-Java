public class Example {
    public static void main(String[] args) {
        // Declare and initialize a byte variable
        byte b = 100;

        // Print the value of the byte variable
        System.out.println("The value of b is: " + b); // Output: The value of b is: 100
    }
}

/*
- A byte data type in Java is a primitive data type used to store integer values.
- It occupies 8 bits (1 byte) of memory.
- It has a range from -128 to 127.
- Byte variables are used when memory conservation is important.
- They are commonly used in situations where memory usage needs to be minimized, 
  such as when working with large arrays or streams of data.
- The default value of a byte variable is 0.
*/
