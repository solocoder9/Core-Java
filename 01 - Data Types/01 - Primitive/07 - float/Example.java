public class Example {
    public static void main(String[] args) {
        // Declare and initialize a float variable
        float f = 3.14f;

        // Print the value of the float variable
        System.out.println("The value of f is: " + f); // Output: The value of f is: 3.14
    }
}

/*
- A float data type in Java is a primitive data type used to store decimal numbers.
- It occupies 32 bits (4 bytes) of memory.
- It has a range from approximately 1.4E-45 to 3.4E+38.
- Float variables are useful when you need to store numbers with fractional parts, but don't require high precision.
- They are commonly used for scientific computations, graphics, and other situations where precision is not critical.
- The default value of a float variable is 0.0f.
*/
