public class Test {
    public static void main(String[] args) {

        // Create an empty string
        String s = "";

        // Check if the string is empty using isEmpty()
        boolean isEmpty = s.isEmpty();

        // Print the result
        System.out.println("Is the string empty? " + isEmpty); // Output: true
    }
}

/*
The isEmpty() method in the String class is used to check if a string is empty, i.e., it has a length of 0. 
It returns true if the string is empty, and false otherwise.
*/
