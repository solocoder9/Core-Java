public class Example {
    public static void main(String[] args) {
        // Declare and initialize a char variable
        char c = 'A';

        // Print the value of the char variable
        System.out.println("The value of c is: " + c); // Output: The value of c is: A
    }
}

/*
- A char data type in Java is a primitive data type used to store a single character.
- It occupies 16 bits (2 bytes) of memory.
- It can store any character from the Unicode character set, including letters, digits, and special characters.
- Char variables are commonly used in Java for representing characters in text processing.
- The default value of a char variable is '\u0000' (null character).
*/
