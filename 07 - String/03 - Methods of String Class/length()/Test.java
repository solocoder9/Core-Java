public class Test {
    public static void main(String[] args) {

        // Create a string
        String s = "Hello, World!";

        // Get the length of the string using length()
        int length = s.length();

        // Print the length of the string
        System.out.println("The length of the string is: " + length); // Output: 13
    }
}

/*
The length() method in the String class is used to get the length of a string, 
i.e., the number of characters in the string. 
It returns an integer representing the length of the string.
*/
